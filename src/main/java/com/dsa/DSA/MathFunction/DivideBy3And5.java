package com.dsa.DSA.MathFunction;

public class DivideBy3And5 {
    public static void main(String[] args) {
        int number=20;
        int divByThree=number/3;
        int divByFive=number/5;
        int mul=number/(3*5);
        int result=divByThree+divByFive-mul;
        System.out.println(result);

    }
}
